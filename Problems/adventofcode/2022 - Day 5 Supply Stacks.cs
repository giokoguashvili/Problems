string Solve(string str)
{
   var result = str.Split(Environment.NewLine);
   var crates = result.TakeWhile(i => i != String.Empty).Reverse().Skip(1).Reverse();
   var steps = result.SkipWhile(i => i != String.Empty).Skip(1);
   var matrix = crates
           .SelectMany(i => i
                               .Chunk(4)
                               .Select(ch => String.Join("", ch).Trim())
                               .Select((e, i) => new { index = i, element = e })
           )
           .Select(x => new { index = x.index, element = x.element != String.Empty ? x.element[1].ToString() : x.element })
           .GroupBy(x => x.index)
           .OrderBy(x => x.Key)
           .Select(x => x.Select(i => i.element).Where(e => !e.IsNullOrEmpty()).ToArray()).ToArray()
           ;
   var newMatrix =
       steps.Select(s =>
       {
           var splited = s.Split(" ").ToArray();
           return new { cnt = Int32.Parse(splited[1]), src = Int32.Parse(splited[3]), dest = Int32.Parse(splited[5]) };
       })
       .Aggregate(matrix, (prev, next) =>
       {
           return Move(next.cnt, next.src - 1, next.dest - 1, prev);
       })
       .Select(x => x.First());
   return String.Join("", newMatrix);
}

string[][] Move(int cnt, int src, int dest, string[][] m)
{
   var temp = m[src].Take(cnt);
   m[src] = m[src].Skip(cnt).ToArray();
   m[dest] = temp.Concat(m[dest]).ToArray();
   return m;
}

